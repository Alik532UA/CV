<script lang="ts">
    import Particles from "./backgrounds/Particles.svelte";
    import Waves from "./backgrounds/Waves.svelte";
    import FloatingShapes from "./backgrounds/FloatingShapes.svelte";
    import { fade } from "svelte/transition";
    import { onMount } from "svelte";

    export let backgroundType: 0 | 1 | 2 | 3 = 1;
    export let theme: string = "dark";

    let fixedHeight = "100vh";
    let lastWidth = 0;

    onMount(() => {
        // Initial set
        fixedHeight = window.innerHeight + "px";
        lastWidth = window.innerWidth;

        const handleResize = () => {
            // Ignore vertical resize (mobile scroll)
            if (window.innerWidth === lastWidth) return;

            lastWidth = window.innerWidth;
            fixedHeight = window.innerHeight + "px";
        };

        window.addEventListener("resize", handleResize);

        return () => {
            if (typeof window !== "undefined") {
                window.removeEventListener("resize", handleResize);
            }
        };
    });
</script>

{#if backgroundType === 1}
    <div
        class="bg-transition"
        style="height: {fixedHeight}"
        transition:fade={{ duration: 800 }}
    >
        <Particles {theme} />
    </div>
{:else if backgroundType === 2}
    <div
        class="bg-transition"
        style="height: {fixedHeight}"
        transition:fade={{ duration: 800 }}
    >
        <Waves {theme} />
    </div>
{:else if backgroundType === 3}
    <div
        class="bg-transition"
        style="height: {fixedHeight}"
        transition:fade={{ duration: 800 }}
    >
        <FloatingShapes {theme} />
    </div>
{/if}

<style>
    .bg-transition {
        position: fixed;
        top: 0;
        left: 0;
        width: 100%;
        /* Height is set via inline style for mobile stability */
        z-index: -1;
        pointer-events: none;
    }
</style>
